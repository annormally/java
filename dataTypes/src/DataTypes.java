public class DataTypes {
    public static void main(String[] args) {

        // Java Data Types
        int integerNumber = 3; // Integer (Whole number)
        float floatNumber = 3.44f; // Floating point number
        char charLetter = 'A'; // Character
        boolean trueOrFalse = false;
        String stringText = "Hello";

        System.out.println("Integer number: " + integerNumber);
        System.out.println("Float number: " + floatNumber);
        System.out.println("Char letter: " + charLetter);
        System.out.println("Boolean: " + trueOrFalse);
        System.out.println("String text: " + stringText);

        // Integer

        // Byte
        byte byteNumber = 100;
        System.out.println(byteNumber);

        // Short
        short shortNumber = 5000;
        System.out.println(shortNumber);

        // int
        int intNumber = 123456;
        System.out.println(intNumber);

        // Long
        long longNumber = 156367890344L;
        System.out.println(longNumber);

        // Floating Point Types

        // Float
        float floatNum = 123456;
        System.out.println(floatNum);

        // Double
        double doubleNum = 156367890344L;
        System.out.println(doubleNum);

        // Scientific Numbers
        // Power of 10
        float float1 = 23e5f;
        double double1 = 18E8d;
        System.out.println(float1);
        System.out.println(double1);

        // Booleans
        boolean isFun = true;
        boolean isNotFun = false;
        System.out.println(isFun);
        System.out.println(isNotFun);

        // Characters
        char character = 'A';
        System.out.println(character);

        // ASCII values to display certain characters
        char a = 65, b = 66, c = 67;
        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
    }
}
