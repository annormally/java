package operator;

public class Operator {
    public static void main(String[] args) {

        // Operator +
        int x = 50 + 50;
        System.out.println("x = " + x);

        int sum1 = 50 + 50;
        int sum2 = sum1 + 300;
        int sum3 = sum2 + sum2;

        System.out.println(sum1);
        System.out.println(sum2);
        System.out.println(sum3);

    }
}
